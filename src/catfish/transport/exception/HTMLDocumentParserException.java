package catfish.transport.exception;

public class HTMLDocumentParserException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 6682301057955759139L;

	public HTMLDocumentParserException() {
		super();
	}

	public HTMLDocumentParserException(String message) {
		super(message);
	}

	public HTMLDocumentParserException(String message, Throwable cause) {
		super(message, cause);
	}

}
